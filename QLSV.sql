  DBMS_RLS.DROP_POLICY(
      object_schema => 'QLSV', 
      object_name   => 'SINHVIEN', 
      policy_name   => 'CHINHSACH_VPD_SV'
  );
EXCEPTION WHEN OTHERS THEN NULL;
END;
/

-- Áp dụng chính sách mới
BEGIN
  DBMS_RLS.ADD_POLICY (
    object_schema    => 'QLSV',
    object_name      => 'SINHVIEN',
    policy_name      => 'CHINHSACH_VPD_SV',
    function_schema  => 'QLSV',
    policy_function  => 'POLICY_XEM_DIEM_CANHAN',
    statement_types  => 'SELECT'
  );
END;
/

