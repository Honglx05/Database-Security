-- 7. Cấu hình VPD (Chính sách bảo mật)
CREATE OR REPLACE FUNCTION POLICY_XEM_DIEM_CANHAN (
  p_schema IN VARCHAR2,
  p_object IN VARCHAR2
) RETURN VARCHAR2 AS
  v_user VARCHAR2(100);
BEGIN
  v_user := SYS_CONTEXT('USERENV', 'SESSION_USER');
  
  IF (v_user = 'GIAOVU' OR v_user = 'QLSV') THEN
      RETURN '1=1'; 
  ELSIF (v_user = 'QLSV1') THEN
      RETURN 'SEC_LEVEL <= 2'; 
  ELSIF (v_user = 'QLSV2') THEN
      RETURN 'SEC_LEVEL = 1'; 
  ELSE
      RETURN 'USERNAME = ''' || v_user || '''';
  END IF;
END;
/

-- Xóa chính sách cũ
